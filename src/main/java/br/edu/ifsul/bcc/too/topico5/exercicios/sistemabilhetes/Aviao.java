
package br.edu.ifsul.bcc.too.topico5.exercicios.sistemabilhetes;

/**
 *
 * @author telmo
 */
public class Aviao {
    
    private Long id;
    private String codigo;
    private double carga;
    private Integer qtdEconomica;
    private Integer qtdPrimeira;
    private Integer qtdExecutiva;

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the carga
     */
    public double getCarga() {
        return carga;
    }

    /**
     * @param carga the carga to set
     */
    public void setCarga(double carga) {
        this.carga = carga;
    }

    /**
     * @return the qtdEconomica
     */
    public Integer getQtdEconomica() {
        return qtdEconomica;
    }

    /**
     * @param qtdEconomica the qtdEconomica to set
     */
    public void setQtdEconomica(Integer qtdEconomica) {
        this.qtdEconomica = qtdEconomica;
    }

    /**
     * @return the qtdPrimeira
     */
    public Integer getQtdPrimeira() {
        return qtdPrimeira;
    }

    /**
     * @param qtdPrimeira the qtdPrimeira to set
     */
    public void setQtdPrimeira(Integer qtdPrimeira) {
        this.qtdPrimeira = qtdPrimeira;
    }

    /**
     * @return the qtdExecutiva
     */
    public Integer getQtdExecutiva() {
        return qtdExecutiva;
    }

    /**
     * @param qtdExecutiva the qtdExecutiva to set
     */
    public void setQtdExecutiva(Integer qtdExecutiva) {
        this.qtdExecutiva = qtdExecutiva;
    }
    
    
    
    
    
}
